package Model;

public class Lieu {
    private int id;
    private String nom;
 

    public void setId(int id) {
        this.id = id;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

  

    public int getId() {
        return id;
    }

    public String getName() {
        return nom;
    }

  
}
